using DIGOS.Ambassador.Core.Services;
using DIGOS.Ambassador.Database.Abstractions.Design;
using DIGOS.Ambassador.Database.Abstractions.Services;
using DIGOS.Ambassador.Plugins.Dossiers.Model;
using JetBrains.Annotations;
using Microsoft.EntityFrameworkCore;
using Microsoft.EntityFrameworkCore.Design;

[assembly: DesignTimeServicesReference("DIGOS.Ambassador.Database.Abstractions.Design." + nameof(AutoGeneratedDesignTimeServices) + ", DIGOS.Ambassador.Database.Abstractions")]

namespace DIGOS.Ambassador.Plugins.Dossiers.Design
{
    /// <summary>
    /// Design-time factory for <see cref="DossiersDatabaseContext"/> instances.
    /// </summary>
    [UsedImplicitly]
    public class DesignTimeDossiersDatabaseContextFactory : IDesignTimeDbContextFactory<DossiersDatabaseContext>
    {
        /// <inheritdoc />
        [NotNull]
        public DossiersDatabaseContext CreateDbContext(string[] args)
        {
            var options = new SchemaAwareDbContextService(new ContentService())
                .ConfigureSchemaAwareContext<DossiersDatabaseContext>(new DbContextOptionsBuilder()).Options;

            return new DossiersDatabaseContext(options);
        }
    }
}
